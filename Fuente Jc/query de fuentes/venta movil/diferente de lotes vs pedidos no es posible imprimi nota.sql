 SELECT SUM(DB_QUANT) D2_QUANT
	 FROM SF2010 SF2 
	 LEFT JOIN SD2010 SD2 ON D2_FILIAL = F2_FILIAL AND D2_DOC = F2_DOC AND D2_SERIE = F2_SERIE AND D2_LOJA = F2_LOJA AND SD2.D_E_L_E_T_ = ' '
	 LEFT JOIN SC6010 SC6 ON C6_FILIAL = D2_FILIAL AND D2_PEDIDO = C6_NUM AND D2_ITEMPV = C6_ITEM AND SC6.D_E_L_E_T_ = ' ' 
	 LEFT JOIN SDB010 SDB ON SDB.DB_FILIAL = SD2.D2_FILIAL AND SDB.DB_PRODUTO = SD2.D2_COD AND SDB.DB_LOCAL = SD2.D2_LOCAL 
	 AND SDB.DB_LOTECTL = SD2.D2_LOTECTL AND SDB.DB_ESTORNO <> 'S' 
	 AND SDB.DB_DOC = 
	 CASE WHEN SD2.D2_REMITO <> '' 
	 THEN SD2.D2_REMITO 
	 ELSE SD2.D2_DOC 
	 END 
	 AND SDB.DB_SERIE = 
	 CASE WHEN SD2.D2_SERIREM <> '' 
	 THEN SD2.D2_SERIREM 
	 ELSE SD2.D2_SERIE 
	 END 
	 AND SDB.DB_NUMSEQ = 
	 CASE WHEN SD2.D2_SERIREM = '' 
	 THEN SD2.D2_NUMSEQ 
	 ELSE (SELECT SD22.D2_NUMSEQ FROM SD2010 ) SD22 WHERE SD22.D_E_L_E_T_='' 
	 AND SD22.D2_FILIAL=SD2.D2_FILIAL AND SD22.D2_ESPECIE='RFN' AND SD22.D2_SERIE=SD2.D2_SERIREM 
	 AND SD22.D2_DOC=SD2.D2_REMITO AND SD22.D2_COD=SD2.D2_COD AND SD22.D2_ITEM=SD2.D2_ITEMREM) 
	 END 
	 AND SDB.D_E_L_E_T_ <> '*' 
	 WHERE SD2.D2_FILIAL  = '0105'
	 AND SD2.D2_DOC = '000000000000000122' AND D2_SERIE = 'S70'
	 AND SD2.D2_ESPECIE = 'NF' 
	 AND SF2.D_E_L_E_T_ = ' '
	 GROUP BY D2_FILIAL,D2_SERIE,D2_DOC 